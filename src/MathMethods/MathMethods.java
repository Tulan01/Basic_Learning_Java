
package MathMethods;


public class MathMethods {
    public static void main(String[] args) {
        System.out.println("Absolute Value "+Math.abs(-2.5));//return the absolute value
        System.out.println("Square Value "+Math.sqrt(4));//return the square value
        System.out.println("Power Value "+Math.pow(2,3));//return the power first argument is value and second one is power value
        System.out.println("Pi Value "+Math.PI);//return the pi value
        System.out.println("Log Value "+Math.log(2));//return the log value
        System.out.println("Exponencial Value "+Math.exp(4));//return the Exponencial value
        System.out.println("Maximum Value "+Math.max(2.5,2.6));//return the Maximum value
        System.out.println("Minimum Value "+Math.min(-2.5,9.8));//return the Minimum value
        System.out.println("Ceiling Value "+Math.ceil(2.6));//return the Ceilling value
        System.out.println("Floor Value "+Math.floor(2.5));//return the Floor value
    }
}
